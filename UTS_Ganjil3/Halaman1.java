    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nama = jTextField5.getText().trim();
        String password = jTextField2.getText().trim();
        String email = jTextField3.getText().trim();
        String telp = jTextField4.getText().trim();
        String backup = jTextField1.getText().trim(); // opsional

        // CEK KOSONG
        if (telp.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Nomor telepon wajib diisi!",
                    "Peringatan",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return;
        }

// CEK ANGKA SAJA
        if (!telp.matches("\\d+")) {
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Nomor telepon harus berupa ANGKA!",
                    "Input Tidak Valid",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        // VALIDASI WAJIB
        if (nama.isEmpty() || password.isEmpty() || email.isEmpty() || telp.isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Nama, Password, Email, dan Nomor Telepon wajib diisi!",
                    "Peringatan",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return; // STOP â†’ tidak lanjut ke Shop
        }

        // BARU BUAT OBJEK
        User user = new User(
                nama,
                password,
                email,
                telp,
                backup
        );

        openShop(user);
    }//GEN-LAST:event_jButton1ActionPerformed

